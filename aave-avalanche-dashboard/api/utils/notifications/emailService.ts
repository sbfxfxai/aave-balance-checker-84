/**
 * Email Notification Service
 * Sends email alerts for critical system issues
 */

import { logger, LogCategory } from '../logger';

interface EmailMessage {
  to: string[];
  subject: string;
  html: string;
  text: string;
}

interface EmailAlert {
  title: string;
  message: string;
  severity: string;
  timestamp: string;
  context?: Record<string, any>;
}

